package thinhhq.model;

/**
 *
 * @author ANDIM
 */
public class tblOrderDTO {
    private String id;
    private String date;
    private float total;

    public tblOrderDTO() {
    }

    public tblOrderDTO(String id, String date, float total) {
        this.id = id;
        this.date = date;
        this.total = total;
    }

    
    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the total
     */
    public float getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(float total) {
        this.total = total;
    }
    
}
